namespace Opo.Net.Mail
{
    /// <summary>
    /// Specifies the priority of a mail message
    /// </summary>
    public enum MailPriority
    {
        /// <summary>
        /// The mail message is sent with low priority (5)
        /// </summary>
        Low = 5,
        /// <summary>
        /// The mail message is sent with normal priority (3)
        /// </summary>
        Normal = 3,
        /// <summary>
        /// The mail message is sent with high priority (1)
        /// </summary>
        High = 1
    }
}
